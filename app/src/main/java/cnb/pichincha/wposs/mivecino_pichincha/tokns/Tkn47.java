package cnb.pichincha.wposs.mivecino_pichincha.tokns;


import newpos.libpay.utils.ISOUtil;

public class Tkn47 {

    private byte[] cedula_ruc = new byte[8];
    private byte[] nombre = new byte[]{0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20};
    private byte[] cuenta = new byte[7];

    public byte[] getCedula_ruc() {
        return cedula_ruc;
    }

    public void setCedula_ruc(byte[] cedula_ruc) {
        this.cedula_ruc = cedula_ruc;
    }

    public byte[] getNombre() {
        return nombre;
    }

    public void setNombre(byte[] nombre) {
        this.nombre = nombre;
    }

    public byte[] getCuenta() {
        return cuenta;
    }

    public void setCuenta(byte[] cuenta) {
        this.cuenta = cuenta;
    }

    /**
     *
     * @return
     */
    public String packTkn47(){
        String tkn47 = "";
        tkn47 = "470045";
        tkn47 += ISOUtil.bcd2str(cedula_ruc, 0, cedula_ruc.length);
        tkn47 += ISOUtil.bcd2str(nombre,0,nombre.length);
        tkn47 += ISOUtil.bcd2str(cuenta, 0, cuenta.length);
        return tkn47;
    }

    public void cleanDeposito(){
        nombre = new byte[] {0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20,0x20};
        cuenta = new byte[] {(byte) 0xFF,(byte) 0xFF,(byte) 0xFF,(byte) 0xFF,(byte) 0xFF,(byte) 0xFF,(byte) 0xFF};
    }

    public void cleanConsultaCredito(){
        cedula_ruc = new byte[] {0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00};
        nombre = new byte[] {0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00};
        cuenta = new byte[] {0x00,0x00,0x00,0x00,0x00,0x00,0x00};
    }
}
